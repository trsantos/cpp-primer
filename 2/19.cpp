// References are an alias to another object. They must be initialized, have
// the same type as the initializer, and cannot change the object to which it
// refers after its creation.

// Pointers are an object on its own. They may or may not be initialized, they
// may be changed to point to another objects, and they can be understood as an
// object which holds the address to another object of the same type -- except
// for void* pointers, these can hold addresses of any object.
